    public void readJTable(){
        
        DefaultTableModel modelo =(DefaultTableModel) Tabela_Ocorrencias.getModel();
        modelo.setNumRows(0);
        //chamando o DAO
        OcorrenciaDAO dao=new OcorrenciaDAO();
        
        for(Ocorrencia c: dao.read()){
            modelo.addRow(new Object []{
                
                c.getData_ocorrencia(),
                c.getAssunto(),
                c.getResposta()
            });
        }
    }
