    private void campo_totalFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_campo_totalFocusGained
        // TODO add your handling code here:
        //CONEXOES COM O BANCO E VAR
        Connection con = connectionFactory.getConnection();
        PreparedStatement stmt = null;
        ResultSet rs = null;
        
        //COMANDOS
        try{
            //COMANDO QUE PREPARA
            stmt = con.prepareStatement("select SUM(id_funcionario) from ocorrencia where id_funcionario=1;");
            rs=stmt.executeQuery();

            if(rs.next()){
                campo_total.setText(rs.getString("SUM(id_funcionario)"));
                
            }
           
        }catch(SQLException ex){
            Logger.getLogger(Tela5_RelatoriosUser1.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        //FINALIZA/SALVA O BANCO
        finally{
            connectionFactory.closeConnection(con, stmt, rs);
        }
    }//GEN-LAST:event_campo_totalFocusGained

